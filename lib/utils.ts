export const generatePasscode = () => {
  // Removed confusing chars like O, 0, I, 1, L, S, 5 to prevent guest frustration
  const chars = "ABCDEFGHJKMNPQRSTUVWXYZ23456789";
  let result = "";
  for (let i = 0; i < 6; i++) {
    result += chars.charAt(Math.floor(Math.random() * chars.length));
  }
  return result;
}